    public void createProcess(){
                if(direc_direcRadio.isSelected())
                {
                    functionManager.createDirectProcess(processCounter, synchronizationTypeProducer, queueType, queueSizeType, 
                                synchronizationTypeReceiver);
  
                }
                else if(direc_indirectRadio.isSelected())
                {
                    CommandTokenizer.getInstance().indirect = true;
                    if(direc_indirecCombo.getSelectedItem().toString()== "Estático")
                    {
                        functionManager.createStaticProcess(processCounter, synchronizationTypeProducer, queueType, queueSizeType, synchronizationTypeReceiver);
                    }else
                    {
                        System.out.println("Proceso Dinamico");
                        functionManager.createDynamicProcess(processCounter, synchronizationTypeProducer, queueType, queueSizeType, synchronizationTypeReceiver);
                    }
                }
                processCounter++;
                createdProcess++; 
    }
    
    
        public void sendMessage(int sourceID, int destinyID, String message, int priority) throws InterruptedException{

                if(direc_direcRadio.isSelected())
                {
                    if(formatCombo.getSelectedItem().toString() == "Fijo")
                    {
                        functionManager.sendDirectProcess(sourceID, destinyID, MessageType.FIXED, Integer.parseInt(formatField.getText())
                                    , message, priority);
                    }else
                    {
                        functionManager.sendDirectProcess(sourceID, destinyID, MessageType.VARIABLE, Integer.parseInt(formatField.getText())
                                , message, priority);
                    }
                }
                else if(direc_indirectRadio.isSelected())
                {
                    if(formatCombo.getSelectedItem().toString() == "Fijo")
                    {
                        functionManager.sendIndirectProcess(sourceID, destinyID, MessageType.FIXED, Integer.parseInt(formatField.getText())
                                , message, priority);
                    }else
                    {
                        functionManager.sendIndirectProcess(sourceID, destinyID, MessageType.VARIABLE, Integer.parseInt(formatField.getText()), message, priority);
                    }
                }
                else{JOptionPane.showMessageDialog(null, "Ha llegado al límite de procesos establecidos"
                        , "Error", JOptionPane.ERROR_MESSAGE);}
    }
        
        
